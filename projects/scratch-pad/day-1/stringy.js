function endsWith(string, char) {
    // YOUR CODE BELOW HERE //
    
    // convert string and char to lower case
    var lowerCaseString = string.toLowerCase();
    var lowerCaseChar = char.toLowerCase();    
    // convert string to array
    lowerCaseString = lowerCaseString.split("");
    // access last index of array with string[string.length - 1] and compare it to char. 
    // if values are equal, return true. return false otherwise.
    if (lowerCaseString[lowerCaseString.length - 1] === lowerCaseChar) {
        return true;
    } else return false;

    // YOUR CODE ABOVE HERE //
}

